package ch04.book;

public class ForPrintFrom1To10Example {
	public static void main(String[] args) {
		for(int i = 1; i<=5; i++) {
			System.out.println(i);
		}
		for(int i = 5; i<=10; i++) {
			System.out.println(i);
		}
		for(int i = 5; i>= 1; i--) {
			System.out.println(i);
		}
		for(int i = 10; i >= 5; i--) {
			System.out.println(i);
		}
	}
}
